// number
console.log(3); // 3
console.log(typeof 10); // number
console.log(typeof 10.5); // number

// string
console.log("i am dip"); // i am dip
console.log("10"); // "10"
console.log(typeof "20"); // string

// boolean
// here true and false special keyword that js understand is it true word, not a string
console.log(false); // false
console.warn(true); // true
console.warn(typeof true); // boolean

// convert string to number
console.log("The number is:" + " " + "20"); // The number is: "20"
console.log(typeof ("The number is:" + " " + "20")); // string

console.log(parseInt("20")); // 20
console.log(typeof parseInt("20")); // number
